            <h2> Public Interest</h2>
            <p>Public interest is a wide and diverse concept that cannot, and should not, be
                limited in its scope and application. The definition of what constitutes public
                interest varies across jurisdictions and should be assessed on a case-by case basis. In its very basic formulation public interest is the notion that an
                action or process or outcome widely and generally benefits the public at large
                (as opposed to a few or a single entity or person) and should be accepted or
                pursued in the spirit of equality and justice</p>
